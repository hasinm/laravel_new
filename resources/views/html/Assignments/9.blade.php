	<h2><a id="1">COMPUTER</a></h2>
	<h3><a href="#7">GO TO TOP</a></h3>
	<p>A computer is a general purpose device that can be programmed to carry out a set of arithmetic or logical operations. Since a sequence of operations can be readily changed, the computer can solve more than one kind of problem.</p>
	<h5><a id="2">cpu</a></h5>
	<p>A Central Processing Unit (CPU), also referred to as a central processor unit,is the hardware within a computer that carries out the instructions of a computer program by performing the basic arithmetical, logical, and input operations of the system. The term has been in use in the computer industry atleast since the early 1960s. The form, design, and implementation of CPUs have changed over the course of their history, but their fundamental operations remain the same.</p>
	<h5><a id="3">ALU</a></h5>
	<p>In computing, an arithmetic and logic unit (ALU) is a digital circuit that performs integer arithmetic and logic operations. The ALU is a fundamental building block of the central processing unit of a computer and even the simplest microprocessors contains one for prposes such as maintaining timers. The processors found inside modern CPUs and graphic processing units (GPUs) accomodate very powerful and very complex ALUs, a single component may contain a number of ALUs.</p>
	<h2><a id="4">PROGRAMMING LANGUAGES</a></h2>
  	<p>A programming language is a formal language designed to communicate instructions to a machine, particularly a computer. Programmoing languages can be used to create programs that control the behaviour of a machine and/or to express algorithms precisely.</p>
  	<h3><a id="6">c<sup>++</sup></a></h3>
  	<p>C++ (pronounced as see plus plus) is a programming language that is general purpose, statically typed, free-form , multi-paradigm and compiled. It is regarded as an immediate-level language, as it comprises both high-level and low-level language features.</p>
  	<h3><a id="5">c</a></h3>
  	<p>C is one of the most widely used proramming languages of all time and C compilers are available for the majority of available computer architectures and operating systems.</p>
